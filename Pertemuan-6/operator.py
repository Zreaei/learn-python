# Operator Identity
# is
x = ["1", "2", "3", "4"]
y = ["1", "2", "3", "4"]
z = x

print(x is y)
print(x is z)

# is not
x = ["1", "2", "3", "4"]
y = ["1", "2", "3", "4"]
z = x

print(x is not y)
print(x is not z)

